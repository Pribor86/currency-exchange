CREATE TABLE IF NOT EXISTS CURRENCY
(
    ID VARCHAR
(
    50
) NOT NULL,
    TYPE_NAME VARCHAR
(
    4
) NOT NULL,
    CURR_DATE VARCHAR
(
    16
) NOT NULL,
    CURRENCY VARCHAR
(
    4
) NOT NULL,
    AMOUNT FLOAT
(
    16
) NOT NULL,
    PRIMARY KEY
(
    ID
)
    );

CREATE TABLE IF NOT EXISTS FX_DATA
(
    id INT NOT NULL AUTO_INCREMENT,
    type_name VARCHAR
(
    4
) NOT NULL,
    curr_date VARCHAR
(
    16
) NOT NULL,
    currency VARCHAR
(
    4
) NOT NULL,
    amount FLOAT NOT NULL,
    PRIMARY KEY
(
    id
)
    );

